package gr.aueb.cf.ch2;

/**
 * Demonstrates the Math class.
 */
public class MathApp {
    public static void main(String[] args) {
        System.out.println("Το απόλυτο του -5 είναι: " + Math.abs(-5));
        System.out.println("Το ελάχιστο του 2, 7 είναι: " + Math.min(2, 7));
        System.out.println("Το max του 2, 7 είναι: " + Math.max(2, 7));
    }
}
